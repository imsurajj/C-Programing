// Program on logical and relational Operators

#include <stdio.h>

int main()
{
    int a = sizeof(int);
    int b = sizeof(char);
    int c = sizeof(float);
    int d = sizeof(double);

    printf("size of int is %u\n", a);
    printf("size of char is %u\n", b);
    printf("size of float is %u\n", c);
    printf("size of double is %u\n", d);

    return 0;
}
